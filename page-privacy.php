            <li>Acesso aos dados: acesso aos dados coletados pela empresa, com exceção dos casos de proteção
              de segredo comercial e indústria;</li>
            <li>Correção de dados: solicitação da correção dos dados que contenham informações incompletas,
              desatualizadas ou errôneas;</li>
            <li>Anonimização e bloqueio: anonimização e bloqueio dos dados desnecessários, excessivos ou
              tratados em desconformidade com o disposto na legislação aplicável. A anonimização se dará
              considerando a utilização de meios técnicos razoáveis e disponíveis na ocasião do tratamento
              dos dados;</li>
            <li>Portabilidade: portabilidade dos dados pessoais mediante requisição expressa. A GABRIELLA
              REVESTIMENTOS CERÂMICOS e suas empresas controladas se reservam ao direito de negar a
              portabilidade em caso de dados pessoais que possam comprometer seus segredos comercial e
              industrial;</li>
            <li>Informação sobre o compartilhamento de dados: informação sobre os dados pessoais que são
              compartilhados com entidades públicas e privadas;</li>
            <li>Revogação de consentimento: revogação do consentimento dado, a qualquer momento, mediante
              requerimento expresso do titular. O procedimento de revogação será sempre gratuito e
              facilitado;</li>
            <li>Eliminação dos dados pessoais: os dados pessoais serão eliminados ao término de sua
              finalidade ou no caso da manifestação do desejo de revogação de seu consentimento para a
              realização daquele tratamento. Sujeito aos requerimentos legais locais, a GABRIELLA
              REVESTIMENTOS CERÂMICOS pode manter o dado pessoal caso: (i) seja legalmente obrigada a
              mantê-los, (ii) para o cumprimento de leis e/ou regulamentos que assim determinem; (iii)
              necessite dos dados para estabelecer, exercer ou defender reivindicações legais; e (iii)
              necessite manter o controle dos dados por razões de saúde pública;</li>
            <li>Descadastramento dos dados pessoais: pode ser solicitado através de e-mail específico;</li>
            <li>Peticionar junto a ANPD: peticionar em relação aos seus dados contra o controlador perante
              autoridade nacional de proteção de dados; mas antes de tomar essa atitude, solicitamos que
              entre em contato com a GABRIELLA REVESTIMENTOS CERÂMICOS pelo e-mail específico, para que
              possamos resolver o problema.</li>
