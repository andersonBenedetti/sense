        <div class="summary-text" id="o-que-sao-cookies">
          <strong>O que são cookies?</strong>

          <p>Cookies são pequenos segmentos de dados, armazenados em arquivos de texto, armazenados em seu
            computador ou outro dispositivo quando sites são carregados em um navegador. Eles são amplamente
            usados para “lembrar” você e suas preferências, seja para uma única visita (por meio de um
            “cookie de sessão) ou para várias visitas repetidas (usando um”cookie persistente”). Eles
            garantem uma experiência consistente e eficiente para os visitantes e executam funções
            essenciais, como permitir que os usuários se registrem e permaneçam conectados. Os cookies podem
            ser definidos pelo site que você está visitando (conhecidos como “cookies primários”) ou por
            terceiros, como aqueles que veiculam conteúdo ou fornecem serviços de publicidade ou análise no
            site (“cookies de terceiros”).</p>

          <p>Os sites e os emails em HTML também podem conter outras tecnologias de rastreamento, como “Web
            beacons” ou “pixels”. Normalmente, são pequenas imagens transparentes que nos fornecem
            estatísticas, para fins semelhantes aos cookies. Geralmente, eles são usados em conjunto com
            cookies, embora não sejam armazenados no seu computador da mesma maneira que os cookies. Como
            resultado, se você desativar cookies, os Web beacons ainda poderão ser carregados, mas suas
            funcionalidades serão restritas.</p>
        </div>

